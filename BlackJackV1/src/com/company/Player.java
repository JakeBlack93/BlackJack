package com.company;

public class Player {
    private String name;
    private int score;
    private int money;

    public String getName() {
        return name;
    }

    public int getScore() {
        return score;
    }

    public int getMoney() {

        return money;
    }

    public void setName(String newName) {
        name = newName;
    }

    public void setScore(int newScore) {
        score = newScore;
    }

    public void setMoney(int newMoney) {
        money = newMoney;
    }





}
